using System;
namespace practica2complejidad
{
    public class Item
    {
        public int peso, valor;
        public Item(int peso, int valor)
        {
            this.peso = peso;
            this.valor = valor;
        }

        public int getpeso() {
            return peso;
        }
        public void setpeso(int peso) {
            this.peso = peso;
        }

        public int getvalor()
        {
            return peso;
        }
        public void setvalor(int valor)
        {
            this.valor = valor;
        }

        
    }
}
